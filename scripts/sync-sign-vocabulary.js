#!/usr/bin/env node
/**
 * Generates backend/data/sign-vocabulary.json from the SIGN_MOTIONS dictionary in
 * frontend/src/components/SignAvatar.js.
 *
 * SignAvatar.js is the single source of truth for what the avatar can physically sign.
 * The backend needs that same list to tell the LLM which words to prefer when glossing.
 * Before this script existed the two were hand-maintained and had drifted badly: the
 * avatar knew 332 words while the LLM prompt listed only 143, so the LLM emitted
 * [CONCEPT:x] (which renders as a silent pause) for 189 words the avatar could sign.
 *
 * Run `npm run sync:vocab` after adding words to SIGN_MOTIONS.
 * backend/__tests__/vocabulary.test.js fails if the generated file goes stale.
 */

const fs = require("fs");
const path = require("path");

const REPO_ROOT = path.join(__dirname, "..");
const SOURCE = path.join(REPO_ROOT, "frontend", "src", "components", "SignAvatar.js");
const OUTPUT = path.join(REPO_ROOT, "backend", "data", "sign-vocabulary.json");

/** Extracts the `const SIGN_MOTIONS = { ... }` object literal by brace matching. */
function extractSignMotionsBlock(source) {
  const marker = "const SIGN_MOTIONS = {";
  const start = source.indexOf(marker);
  if (start === -1) throw new Error(`Could not find "${marker}" in ${SOURCE}`);

  const open = source.indexOf("{", start);
  let depth = 0;
  let inString = null;
  let inLineComment = false;

  for (let i = open; i < source.length; i += 1) {
    const ch = source[i];
    const next = source[i + 1];

    if (inLineComment) {
      if (ch === "\n") inLineComment = false;
      continue;
    }
    if (inString) {
      if (ch === "\\") i += 1;
      else if (ch === inString) inString = null;
      continue;
    }
    if (ch === "/" && next === "/") { inLineComment = true; continue; }
    if (ch === '"' || ch === "'") { inString = ch; continue; }
    if (ch === "{") depth += 1;
    else if (ch === "}") {
      depth -= 1;
      if (depth === 0) return source.slice(open, i + 1);
    }
  }
  throw new Error("Unterminated SIGN_MOTIONS object literal");
}

/**
 * Words the avatar has a motion for but that should not be offered to the LLM as
 * gloss targets. ASL gloss has no contractions and no filler interjections — listing
 * them would invite the LLM to emit non-ASL tokens. The motions stay in SignAvatar.js
 * so that captions containing them still render if they arrive by another path.
 */
const EXCLUDED_FROM_PROMPT = new Set([
  "IM", "ILL", "THATS", "YOUVE", "YOURE", // contractions — not ASL gloss tokens
  "YEAH", "WELL", "BASICALLY",            // spoken filler, carries no ASL meaning
  "SUBSCRIBE",                            // YouTube boilerplate, not lecture content
]);

function main() {
  const source = fs.readFileSync(SOURCE, "utf8");
  const block = extractSignMotionsBlock(source);

  // Top-level keys of SIGN_MOTIONS: `WORD: { label: ... }`
  const words = [...block.matchAll(/^\s{2}([A-Z][A-Z0-9]*):\s*\{\s*label:/gm)].map((m) => m[1]);
  if (words.length === 0) throw new Error("Parsed SIGN_MOTIONS but found no entries");

  const unique = [...new Set(words)].sort();
  const promptVocab = unique.filter((w) => !EXCLUDED_FROM_PROMPT.has(w));

  const payload = {
    $comment: "GENERATED by scripts/sync-sign-vocabulary.js — do not edit by hand. Run `npm run sync:vocab`.",
    generatedFrom: "frontend/src/components/SignAvatar.js",
    totalSignable: unique.length,
    excludedFromPrompt: [...EXCLUDED_FROM_PROMPT].sort(),
    // `vocabulary` is the LLM-facing list (excludes contractions/filler). `signable` is every
    // word the avatar can physically render, used by the backend's enforceSignability pass to
    // decide whether a glossed word needs wrapping as [CONCEPT:x]. Contractions render fine, so
    // they belong in `signable` even though we don't invite the LLM to emit them.
    signable: unique,
    vocabulary: promptVocab,
  };

  fs.mkdirSync(path.dirname(OUTPUT), { recursive: true });
  fs.writeFileSync(OUTPUT, JSON.stringify(payload, null, 2) + "\n");

  console.log(`Signable words in SignAvatar.js: ${unique.length}`);
  console.log(`Excluded from LLM prompt:        ${unique.length - promptVocab.length}`);
  console.log(`Written to sign-vocabulary.json: ${promptVocab.length}`);
  console.log(`  -> ${path.relative(REPO_ROOT, OUTPUT)}`);
}

if (require.main === module) main();

module.exports = { extractSignMotionsBlock, EXCLUDED_FROM_PROMPT };
